from __future__ import annotations

from collections import Counter
from pathlib import Path
from typing import Any

from agentic_decision_benchmark.schemas import MODE_NAMES
from agentic_decision_benchmark.utils.json_utils import to_jsonable


def _table(headers: list[str], rows: list[list[Any]]) -> str:
    header = "| " + " | ".join(headers) + " |"
    separator = "| " + " | ".join("---" for _ in headers) + " |"
    body = ["| " + " | ".join(str(cell) for cell in row) + " |" for row in rows]
    return "\n".join([header, separator] + body)


def _strategy_name(strategies: dict[str, Any], strategy_id: str | None) -> str:
    if not strategy_id:
        return "unresolved"
    details = strategies.get(strategy_id, {})
    return f"{strategy_id}: {details.get('name', strategy_id)}"


def _mode_rows(states: dict[str, dict[str, Any]], strategies: dict[str, Any]) -> list[list[Any]]:
    rows = []
    for mode in MODE_NAMES:
        state = states.get(mode)
        if not state:
            rows.append([mode, "not run", "-", "-", "-"])
            continue
        final = state.get("final_recommendation", {})
        metrics = state.get("metrics", {})
        evaluation = state.get("evaluation", {})
        rows.append(
            [
                mode,
                _strategy_name(strategies, final.get("recommended_strategy")),
                metrics.get("model_calls", "-"),
                metrics.get("total_estimated_tokens", "-"),
                evaluation.get("overall", "-"),
            ]
        )
    return rows


def _self_blackboard_summary(state: dict[str, Any] | None) -> str:
    if not state:
        return "The `self_organizing` mode was not run."
    blackboard = [to_jsonable(item) for item in state.get("blackboard", [])]
    counts = Counter(item.get("item_type") for item in blackboard)
    rows = [[key, value] for key, value in sorted(counts.items())]
    return _table(["Item type", "Count"], rows)


def _items_by_type(state: dict[str, Any] | None, item_type: str, limit: int = 8) -> list[str]:
    if not state:
        return []
    items = []
    for item in [to_jsonable(entry) for entry in state.get("blackboard", [])]:
        if item.get("item_type") == item_type:
            author = item.get("author", "?")
            content = item.get("content", "")
            items.append(f"- {author}: {content}")
        if len(items) >= limit:
            break
    return items


def _scorecard_summary(state: dict[str, Any] | None) -> str:
    if not state:
        return "No scorecards were produced."
    final = state.get("final_recommendation", {})
    aggregate = final.get("aggregate_scores", {})
    if not aggregate:
        return "No aggregate MCDA scores were produced."
    rows = []
    for strategy, score in aggregate.items():
        rows.append([strategy, score.get("mean"), score.get("stdev"), score.get("top_votes")])
    return _table(["Strategy", "Mean", "Stdev", "Top votes"], rows)


def _salience_summary(state: dict[str, Any] | None) -> str:
    if not state:
        return "No salience map was produced."
    metrics = state.get("metrics", {})
    rows = [
        [
            item.get("item_id"),
            item.get("salience"),
            item.get("author"),
            item.get("item_type"),
            str(item.get("content", ""))[:90],
        ]
        for item in metrics.get("top_salience_items", [])
    ]
    if not rows:
        return "No salience records were produced."
    return "\n".join(
        [
            f"Average salience: {metrics.get('average_salience', '-')}",
            "",
            _table(["Item", "Salience", "Author", "Type", "Content"], rows),
        ]
    )


def _conflict_summary(state: dict[str, Any] | None) -> str:
    if not state:
        return "No conflict map was produced."
    metrics = state.get("metrics", {})
    conflict_map = [to_jsonable(item) for item in state.get("conflict_map", [])]
    rows = [
        [
            item.get("conflict_type"),
            item.get("topic"),
            item.get("related_strategy") or "-",
            item.get("severity"),
            item.get("status"),
            str(item.get("summary", ""))[:100],
        ]
        for item in conflict_map[:10]
    ]
    header = (
        "The deterministic conflict map is derived from structured blackboard artifacts, "
        "not manually assigned and not generated by a hidden LLM analyst."
    )
    counts = (
        f"Total conflicts: {metrics.get('number_of_conflicts', '-')}; "
        f"direct critiques: {metrics.get('number_of_direct_critique_conflicts', '-')}; "
        f"opposing stances: {metrics.get('number_of_opposing_stance_conflicts', '-')}; "
        f"risk clusters: {metrics.get('number_of_risk_cluster_conflicts', '-')}."
    )
    if not rows:
        return "\n\n".join([header, counts, "No conflicts were detected."])
    return "\n\n".join([header, counts, _table(["Type", "Topic", "Strategy", "Severity", "Status", "Summary"], rows)])


def _convergence_summary(state: dict[str, Any] | None) -> str:
    if not state:
        return "No convergence status was produced."
    convergence = to_jsonable(state.get("convergence", {}))
    if not convergence:
        return "No convergence status was produced."
    return "\n".join(
        [
            f"Cycle: {convergence.get('cycle_id', '-')}",
            f"Converged: {convergence.get('converged', '-')}",
            f"Leading strategy: {convergence.get('leading_strategy', '-')}",
            f"Agreement ratio: {convergence.get('agreement_ratio', '-')}",
            f"Low-confidence agents: {', '.join(convergence.get('low_confidence_agents', [])) or '-'}",
            f"Reason: {convergence.get('reason', '-')}",
        ]
    )


