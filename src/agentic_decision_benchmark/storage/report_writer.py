        "",
        "The `self_organizing` mode also distributes private information across domain agents. Agents can only expose their private knowledge to others by posting structured artifacts to the blackboard.",
        "",
        "The private role briefs are synthetic because EuroTech is a fictional case. They are used to simulate distributed organizational knowledge and are explicitly labeled as synthetic.",
        "",
